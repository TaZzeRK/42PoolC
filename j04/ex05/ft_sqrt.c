int		ft_sqrt(int nb)
{
	int r;
	int sqrt;

	r = 1;
	sqrt = 0;
	while (r <= nb / 2)
	{
		sqrt = r * r;
		if (sqrt == nb)
			return (r);
		else
			r++;
	}
	return (0);
}
