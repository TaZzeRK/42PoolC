#include "../includes/opp.h"

int		addition(int op1, int op2)
{
	return (op1 + op2);
}

int		substraction(int op1, int op2)
{
	return (op1 - op2);
}

int		division(int op1, int op2)
{
	return (op1 / op2);
}

int		modulo(int op1, int op2)
{
	return (op1 % op2);
}

int		multiplication(int op1, int op2)
{
	return (op1 * op2);
}
